-- create table - STUDENT
CREATE TABLE STUDENT(STU_ID VARCHAR(8) NOT NULL, STU_NAME VARCHAR(20),C_ID VARCHAR(3),STU_PWD VARCHAR(10));

-- create table - TEACHER
CREATE TABLE TEACHER(TEA_ID VARCHAR(8) NOT NULL,TEA_NAME VARCHAR(20),TEA_PWD VARCHAR(10));

-- create table - SUBJECT
CREATE TABLE SUBJECT(SUB_ID VARCHAR(12) NOT NULL, SUB_NAME VARCHAR(30));

-- create table - CLASS
CREATE TABLE CLASS(C_ID VARCHAR(3) NOT NULL,INSTRUCTOR_ID VARCHAR(12));

-- create table - EXAM
CREATE TABLE EXAM(TEST# VARCHAR(11) NOT NULL,TEST_DATE DATE,START_TIME DATE,DURATION VARCHAR(8));

-- create table - QUESTION
CREATE TABLE QUESTION(TEST# VARCHAR(11) NOT NULL, Q# VARCHAR(10) NOT NULL,COMPULSORY VARCHAR(2),TYPE VARCHAR(20),SCORE NUMBER(4,2),Q_CONTENT VARCHAR(1000),ANSWER VARCHAR(1000));

-- create table - TEACH
CREATE TABLE TEACH(YEAR NUMBER(4) NOT NULL, SEM VARCHAR(5) NOT NULL, TEA_ID VARCHAR(8) NOT NULL,SUB_ID VARCHAR(12),C_ID VARCHAR(3) NOT NULL);

-- create table - CHOOSE
CREATE TABLE CHOOSE(YEAR NUMBER(4) NOT NULL, SEM VARCHAR(5) NOT NULL,C_ID VARCHAR(3) NOT NULL,SUB_ID VARCHAR(12) NOT NULL);

-- create table - SETE
CREATE TABLE SETE(TEST# VARCHAR(11) NOT NULL,YEAR NUMBER(4), SEM VARCHAR(5),TEA_ID VARCHAR(8),C_ID VARCHAR(3));

-- create table - ANSWER
CREATE TABLE ANSWER(TEST# VARCHAR(11) NOT NULL,Q# VARCHAR(10) NOT NULL,STU_ID VARCHAR(8) NOT NULL,STU_ANS VARCHAR(1000),STU_SCORE NUMBER(4,2));

-- create table - TAKE
CREATE TABLE TAKE(TEST# VARCHAR(11) NOT NULL,STU_ID VARCHAR(8) NOT NULL,TEST_RESULT NUMBER(5,2), COMMENTS VARCHAR(500));

